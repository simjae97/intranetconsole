document.addEventListener("DOMContentLoaded", function() {
    // 모든 메뉴를 가져오기
    const menuItems = document.querySelectorAll('.gnb > li');

    menuItems.forEach(menuItem => {
        const menuButton = menuItem.querySelector('.btn-menu');
        const subMenu = menuItem.querySelector('.dep2');

        if (menuButton && subMenu) {
            // 메뉴 버튼 클릭 시
            menuButton.addEventListener('click', function(e) {
                e.preventDefault(); // 링크 기본 동작 방지

                // 현재 클릭한 메뉴의 서브메뉴 토글
                if (subMenu.style.display === 'block') {
                    subMenu.style.display = 'none';
                } else {
                    // 다른 메뉴는 닫기
                    menuItems.forEach(otherItem => {
                        const otherSubMenu = otherItem.querySelector('.dep2');
                        if (otherSubMenu && otherSubMenu !== subMenu) {
                            otherSubMenu.style.display = 'none';
                        }
                    });
                    subMenu.style.display = 'block';
                }
            });
        }
    });
});